package br.app.model;

public class Municipio {

	private String Nome;
	private int Populacao;

	public int getPopulacao() {
		return Populacao;
	}

	public void setPopulacao(int populacao) {
		Populacao = populacao;
	}

	public String getNome() {
		return Nome;
	}

	public void setNome(String nome) {
		Nome = nome;
	}

}
